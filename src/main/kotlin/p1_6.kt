fun main(args: Array<String>) {

    val number1 = 12.5
    val number2 = 3.5
    var result: Double

    result = number1 + number2
    println("Addition of 12.5 + 3.5 = $result")

    result = number1 - number2
    println("Subtraction of 12.5 - 3.5 = $result")

    result = number1 * number2
    println("Multiplication of 12.5 * 3.5 = $result")

    result = number1 / number2
    println("Division of 12.5 / 3.5 = $result")


}